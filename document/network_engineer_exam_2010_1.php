                    FR（Frame Relay，帧中继）是八十年代发展起来的一种数据通信技术，它是从X.25分组交换技术演变而来的。<br />
                    FR向用户提供面向连接的通信服务。FR省略了帧编号、差错控制、流量控制、应答、监视等功能，把这些功能全部交给用户终端去完成，大大节省了交换机的开销，降低了时延，提高了信息吞吐量。FR具有高带宽和高可靠性的优点，可以作为X.25的替代方案。<br />
