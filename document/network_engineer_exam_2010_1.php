                    DES是一个分组加密算法，它以64位为分组对数据加密。同时DES也是一个对称算法，即加密和解密用的是同一个算法。它的密钥长度是64位，但实际有效的密钥只是56位，这是因为密钥中每8位就有1位用作奇偶校验。<br />
